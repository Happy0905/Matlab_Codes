% --- Executes on button press in togglebutton1.
function togglebutton1_Callback(hObject, eventdata, handles)
% hObject    handle to togglebutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

% Hint: get(hObject,'Value') returns toggle state of togglebutton1
global status;
statusB = get(hObject,'Value');
if statusB == 1
   % start
   disp('starting');
   status = 1;
   while status == 1
       axes(handles.axes1);
       Fs = str2num(get(handles.edit1,'String'));
       N = str2num(get(handles.edit4,'String'));
       % disp(N)
       C = str2num(get(handles.edit5,'String'));
       recObj=audiorecorder(Fs,N,C);
       recordblocking(recObj,1);
       % play(recObj);
       y1=getaudiodata(recObj);
       x = linspace(0,1,length(y1));
       plot(x, y1),xlabel('Time(s)'),ylabel('Amplitude'),title('FM Signal'),...
           grid on; %plot signal
       
       %plotting fft graph
       axes(handles.axes2);
       plot(abs(fft(y1))),xlabel('Frequency(Hz)'),ylabel('Amplitude'),title('Fourier Transform'),...
        grid on; %plot fft signal
    
       drawnow; %display result
       pause(0.01);
   end
else
    % do nothing
    disp('Stopping');
    status = 0;
end


